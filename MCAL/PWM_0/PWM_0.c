#include <avr/io.h>
#include "typedef.h"
#include "common_macros.h"
#include "pwm_0.h"

//********************************************************************************************************************************************
//********************************************************************************************************************************************
//START OF DRIVER OD PWM_0
void PWM0_init()
{
	SET_BIT(DDRB,3);
	TCCR0 = (1<<WGM01) | (1<<WGM00) |  (1<<COM01) | (1<<CS00);
	TCNT0 = 0;

}
//***************************************************************
//***************************************************************
void set_dutycycle( uint_8 d_cycle)
{
	OCR0 = (uint_8) ( ( (uint_16)(d_cycle * 256) ) / 100 );
}
//***************************************************************
//***************************************************************
void set_prescaler( uint_8 N)
{
	CLEAR_BIT(TCCR0,0);
	TCCR0  |= N;
}
//END OF DRIVER OF PWM_0
